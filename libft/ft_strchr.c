#include "libft.h"

char	*ft_strchr(const char *a, int c)
{
	int		i;
	char	*s;

	i = 0;
	s = (char *)a;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			return (&s[i]);
		i++;
	}
	if ((char)c == '\0')
		return (&s[i]);
	return (NULL);
}
